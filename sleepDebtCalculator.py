def getSleepHours(day):
    match day:
        case 'monday':
            return 6
        case 'tuesday':
            return 5
        case 'wednesday':
            return 4
        case 'thursday':
            return 5
        case 'friday':
            return 9 
        case 'saturday':
            return 10
        case 'sunday':
            return 7

